"""
Data Representation

Number Systems:-
Decimal
Binary
Octal
Hexadecimal
"""

number = 786
print("number in decimal:", number)
print("number in binary is L:", bin(number))
print("number in octal is:", oct(number))
print("number in hexadecimal is:", hex(number))

print()

print("Hashcode in decimal", id(number))
print("Hashcode in binary", bin(id(number)))
print("Hashcode in octal", oct(id(number)))
print("Hashcode in hexadecimal", hex(id(number)))
